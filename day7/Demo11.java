package day7;

public class Demo11 {

	public static void main(String[] args) {
		
		int a = 10;
		System.out.println(a++);
		System.out.println(a);
		
		int b=10;
		System.out.println(++b);
		System.out.println(b);
		
		int c=10;
		System.out.println(c--);
		System.out.println(c);
		
		int d=10;
		System.out.println(--d);
		System.out.println(d);
	}

}
